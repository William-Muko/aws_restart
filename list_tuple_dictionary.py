# List Data Type
myFruitList= ["apple" , "banana" , "cherry"]
print(myFruitList) 
print(type(myFruitList)) 


#Access a List by Position
print(myFruitList[0]) 
print(myFruitList[1]) 
print(myFruitList[2]) 

      
#Changing the Values in a List
myFruitList[2] = "orange"
print(myFruitList) 



#The Tuple Data Type
myFinalAnswerTuple = ("apple", "banana", "pineapple") 
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))


#Access a Tuple by Position
print(myFinalAnswerTuple[0]) 
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2]) 


#The Dictionary Data Type
myFavoriteFruitDictionary = { "Adam" : "apple",
                             "Ben" : "banana",
                             "Penny" : "pineapple"
                            } 
print(myFavoriteFruitDictionary) 
print(type(myFavoriteFruitDictionary)) 


#Accessing a Dictionary by Name
print(myFavoriteFruitDictionary["Adam"])
print(myFavoriteFruitDictionary["Ben"])
print(myFavoriteFruitDictionary["Penny"])