# Numeric Data Types
print("Python has 3 numeric types: int, float, complex")

#print numeric data of type int
myValue=1
print(myValue)
print(type(myValue))
print(str(myValue) + " is of data type " + str(type(myValue)))


#print numeric data of type float
myValue=3.14
print(myValue)
print(type(myValue))
print(str(myValue) + " is of data type " + str(type(myValue)))


#print numeric data of type complex
myValue=5j
print(myValue)
print(type(myValue))
print(str(myValue) + " is of data type " + str(type(myValue)))


# Booleans are implemented as a subclass of integers.(True or False)
myValue=True
print(myValue)
print(type(myValue))
print(str(myValue) + " is of data type " + str(type(myValue)))


myValue=False
print(myValue)
print(type(myValue))
print(str(myValue) + " is of data type " + str(type(myValue)))